void UpdatePosition()
{
	static uint64_t TimestampCalcualtionLoop=0;
	if(micros()-TimestampCalcualtionLoop >= 1000 )	// 1kHz loop for calculate pos
	{
		uint16_t RawRead = TIM1->CNT;		//get qei counter from timer1
		DegAbs = ((RawRead % 3072)/3072.0)*360.0;	//calculate absolute degree of motor
		Deg_unwrap = Unwrapping(DegAbs);	// calculate degree unwrap for finding error in control loop
		TimestampCalcualtionLoop =  micros();
	}
}
float Unwrapping(float InputP)
{
	static int Pmax = 360; //360*1
	static float threshold = 360*0.5;

    if (InputP - plast <= -threshold){
        po = polast + Pmax;
    }
    else if (InputP - plast >= threshold){
        po = polast - Pmax;
    }
    else {
        po = polast;
    }
    plast = InputP;		// P feedback
    polast = po;		// Po feedback
    return InputP + po;
}
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim == &htim2)
	{
		_micros += 4294967295;
	}
	if (htim == &htim3)		//1kHz control loop interrupt
	{
		UpdatePosition();	//read encoder and store in Deg_unwrap
		update(Deg_unwrap);	//predict and update
	}
}
uint64_t micros()
{
	return _micros + htim2.Instance->CNT;
}
void predict()
{
    X = A*X + B*U;
    P = A*P*(A.transpose()) + G*Q*(G.transpose());
}
void update(float position)
{
	predict();
	Y(0,0) = position;	// assign value of encoder
	K = P*(C.transpose()) * (C*P*(C.transpose()) + R).inverse();
	X = X + K*(Y - C*X);	// get new X(estimated state)
	P = (MatrixXf::Identity(3,3) - K*C) * P;
	pos_est = X(0,0);	// get estimated position
	vel_est = X(1,0);	// get estimated velocity
	acc_est = X(2,0);	// get estimated acceleration

	lag = pos_est - position;	//calculate lag
}
